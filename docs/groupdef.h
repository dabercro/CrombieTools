/**
   @defgroup analysisgroup Analysis Tools
   Users can access objects via the CrombieTools.AnalysisTools module.
   Tools to do slightly more sophisticated analyses than the [Plot Tools](@ref plotgroup). */

/**
   @defgroup commongroup Common Tools
   Users can access objects via the CrombieTools.CommonTools module.
   These tools are mostly just inherited by many different modules within the package. */

/**
   @defgroup plotgroup Plot Tools
   Users can access objects via the CrombieTools.PlotTools module.
   Tools for making plots (and nothing else). */

/**
   @defgroup skimminggroup Skimming Tools
   Users can access objects via the CrombieTools.SkimmingTools module.
   Tools skimming flat trees, along with applying corrections. */
